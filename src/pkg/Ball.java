package pkg;

public class Ball {
	int width;
	int height;
	int speed;
	public Ball(int width, int height, int speed) {
		super();
		this.width = width;
		this.height = height;
		this.speed = speed;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
}
